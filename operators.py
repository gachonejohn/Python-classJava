#arithmetic operator
a = 7
b = 3

print(f"Additional operator: {a + b}")
print(f"subtraction operator: {a - b}")
print(f"Division operator: {a / b}")
print(f"Remainder(modulus) operator: {a % b}")
print(f"Exponent(power) operator: {a ** b}")
print(f"Floor(rounded off) division operator: {a // b}")

#comparison operators
c =56
d =32

print(f"{c} is equal to {d}: {c==d}")
print(f"{c} is not equal to {d}: {c!=d}")
print(f"{c} is grater than {d}: {c>d}")
print(f"{c} is not less than {d}: {c<d}")

